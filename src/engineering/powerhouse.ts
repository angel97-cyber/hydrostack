/**
 * Recommends a turbine type based on the net head boundaries.
 * 
 * @param H_net Net design head in meters
 * @param Q Net design flow rate in m3/s
 * @returns Recommended turbine type
 */
export function recommendTurbineType(H_net: number, Q: number): 'Pelton' | 'Francis' | 'Kaplan' | 'Propeller' {
  if (H_net > 250) {
    return 'Pelton';
  } else if (H_net >= 30) {
    return 'Francis';
  } else {
    // Under 30m, choose Kaplan for larger flows, Propeller for lower flows
    return Q >= 5.0 ? 'Kaplan' : 'Propeller';
  }
}

/**
 * Calculates the synchronous generator speed and number of poles.
 * 
 * @param theoretical_N Theoretical turbine rotational speed in rpm
 * @param frequency_Hz Electrical grid frequency (default 50 Hz for Nepal)
 * @returns Synchronous speed and pole count
 */
export function calculateSynchronousSpeed(
  theoretical_N: number,
  frequency_Hz: number = 50
): { synchronousSpeed_N: number; poles: number } {
  if (theoretical_N <= 0) {
    return { synchronousSpeed_N: 0, poles: 0 };
  }

  // Number of poles: p = 2 * round(60 * f / N)
  // Ensure we round to the nearest even number (at least 2 poles)
  const exactPoles = (120 * frequency_Hz) / theoretical_N;
  let poles = 2 * Math.round(exactPoles / 2);
  if (poles < 2) {
    poles = 2;
  }

  const synchronousSpeed_N = (120 * frequency_Hz) / poles;
  return { synchronousSpeed_N, poles };
}

/**
 * Performs piecewise linear interpolation on the tabulated turbine efficiency curves.
 * 
 * @param turbineType Selected turbine type ('Pelton' | 'Francis')
 * @param percentLoad Operating load percentage (0 - 100)
 * @returns Interpolated efficiency (percentage value, e.g. 92.5)
 */
export function getTurbineEfficiency(
  turbineType: 'Pelton' | 'Francis',
  percentLoad: number
): number {
  if (percentLoad <= 0) {
    return 0;
  }

  const curve = turbineType === 'Pelton' ? PT_CURVE : FT_CURVE;
  // Sort curve by load ascending to ensure interpolation works correctly
  const sorted = [...curve].sort((a, b) => a.load - b.load);
  const n = sorted.length;
  if (n === 0) return 0;

  // Clamp out of bounds
  if (percentLoad <= sorted[0].load) {
    return sorted[0].eff;
  }
  if (percentLoad >= sorted[n - 1].load) {
    return sorted[n - 1].eff;
  }

  // Linear interpolation
  for (let i = 0; i < n - 1; i++) {
    const p1 = sorted[i];
    const p2 = sorted[i + 1];
    if (percentLoad >= p1.load && percentLoad <= p2.load) {
      const fraction = (percentLoad - p1.load) / (p2.load - p1.load);
      return p1.eff + fraction * (p2.eff - p1.eff);
    }
  }

  return 0;
}

/**
 * Computes theoretical mechanical shaft power and electrical power output.
 * 
 * @param Q Flow rate in m3/s
 * @param H_net Net design head in meters
 * @param turbineEff Turbine efficiency as a percentage (e.g. 90.0) or fraction (e.g. 0.9)
 * @param generatorEff Generator efficiency as a percentage (e.g. 96.0) or fraction (e.g. 0.96)
 * @returns Mechanical and electrical power output in kW
 */
export function calculatePowerOutput(
  Q: number,
  H_net: number,
  turbineEff: number,
  generatorEff: number
): { shaftPower_kW: number; electricalPower_kW: number } {
  // Normalize efficiencies if passed as percentage values (i.e. > 1.0)
  const n_t = turbineEff > 1.0 ? turbineEff / 100.0 : turbineEff;
  const n_g = generatorEff > 1.0 ? generatorEff / 100.0 : generatorEff;

  // P_m = 9.81 * Q * H_net * eta_t (kW)
  const shaftPower_kW = 9.81 * Q * H_net * n_t;
  const electricalPower_kW = shaftPower_kW * n_g;

  return { shaftPower_kW, electricalPower_kW };
}

export interface BoQCostItem {
  name: string;
  quantity: number;
  rate: number;
  cost: number;
}

/**
 * Maps concrete and steel quantities to rates and compiles a cost estimation sheet.
 * 
 * @param components Quantities for desander concrete, penstock steel, and anchor block concrete
 * @param rates Unit rates for concrete (per m3) and steel (per tonne)
 * @returns Itemized cost sheet and total cost
 */
export function compileBoQAndCost(
  components: {
    desanderConcrete_m3: number;
    penstockSteel_tonnes: number;
    anchorBlockConcrete_m3: number;
  },
  rates: Record<string, number>
): { items: BoQCostItem[]; totalCost: number } {
  const concreteRate = rates['concrete'] || 0.0;
  const steelRate = rates['steel'] || 0.0;

  const items: BoQCostItem[] = [
    {
      name: 'Desanding Basin Concrete',
      quantity: components.desanderConcrete_m3,
      rate: concreteRate,
      cost: components.desanderConcrete_m3 * concreteRate
    },
    {
      name: 'Penstock Structural Steel',
      quantity: components.penstockSteel_tonnes,
      rate: steelRate,
      cost: components.penstockSteel_tonnes * steelRate
    },
    {
      name: 'Anchor Blocks Concrete',
      quantity: components.anchorBlockConcrete_m3,
      rate: concreteRate,
      cost: components.anchorBlockConcrete_m3 * concreteRate
    }
  ];

  const totalCost = items.reduce((sum, item) => sum + item.cost, 0);

  return { items, totalCost };
}
